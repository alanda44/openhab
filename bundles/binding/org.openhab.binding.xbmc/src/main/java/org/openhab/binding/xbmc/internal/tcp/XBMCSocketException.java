package org.openhab.binding.xbmc.internal.tcp;

@SuppressWarnings("serial")
public class XBMCSocketException extends Exception {

	public XBMCSocketException() {
		super();

	}

	public XBMCSocketException(String message, Throwable cause) {
		super(message, cause);

	}

	public XBMCSocketException(String message) {
		super(message);

	}

	public XBMCSocketException(Throwable cause) {
		super(cause);

	}

}
